package pokemon;



public class muniemon {
	 String nombre;
	 int vida;
	 int ataque;
	 int defensa;
	Tipo tipo;



	public muniemon() {
		super();
	}



	public muniemon(String nombre, int vida, int ataque, int defensa, Tipo tipo) {
		super();
		this.nombre = nombre;
		this.vida = vida;
		this.ataque = ataque;
		this.defensa = defensa;
		this.tipo = tipo;
	}

	
	}



